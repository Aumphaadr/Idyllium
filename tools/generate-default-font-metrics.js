#!/usr/bin/env node
'use strict';

const fs = require('fs');
const path = require('path');
const fontkit = require('fontkit');

const rootDir = path.resolve(__dirname, '..');
const fontPath = path.join(rootDir, 'packages', 'gui-renderer', 'fonts', 'SourceCodePro-Regular.woff2');
const outputPath = path.join(rootDir, 'src', 'runtime', 'default-font-metrics.ts');

if (!fs.existsSync(fontPath)) {
  console.error(`Default Canvas font was not found: ${fontPath}`);
  process.exit(1);
}

const font = fontkit.create(fs.readFileSync(fontPath));
const codePoints = [...font.characterSet].sort((left, right) => left - right);
if (codePoints.length === 0) {
  console.error('Default Canvas font does not contain any characters.');
  process.exit(1);
}

const advances = new Set(codePoints.map((codePoint) => font.glyphForCodePoint(codePoint).advanceWidth));
if (advances.size !== 1) {
  console.error('Default Canvas font must be monospaced so its built-in metrics stay compact.');
  process.exit(1);
}

const ranges = [];
let start = codePoints[0];
let end = start;
for (const codePoint of codePoints.slice(1)) {
  if (codePoint === end + 1) {
    end = codePoint;
    continue;
  }
  ranges.push([start, end]);
  start = codePoint;
  end = codePoint;
}
ranges.push([start, end]);

const source = `/* Generated by tools/generate-default-font-metrics.js. */
export const DEFAULT_FONT_UNITS_PER_EM = ${font.unitsPerEm};
export const DEFAULT_FONT_ADVANCE = ${[...advances][0]};

const DEFAULT_FONT_CODE_POINT_RANGES: readonly (readonly [number, number])[] = ${JSON.stringify(ranges)};

export function defaultFontHasCodePoint(codePoint: number): boolean {
  let low = 0;
  let high = DEFAULT_FONT_CODE_POINT_RANGES.length - 1;
  while (low <= high) {
    const middle = Math.floor((low + high) / 2);
    const [start, end] = DEFAULT_FONT_CODE_POINT_RANGES[middle];
    if (codePoint < start) high = middle - 1;
    else if (codePoint > end) low = middle + 1;
    else return true;
  }
  return false;
}
`;

fs.writeFileSync(outputPath, source, 'utf8');
console.log(`Default Canvas font metrics written to ${path.relative(rootDir, outputPath)}`);
